public class Belajar05 {
    public static void main(String[] args) {
        int A,B;
        A = 5;
        B = 2;
        
        //Inkremen
        A++;
        ++B;
        System.out.println("Inkremen A = " + A);
        System.out.println("Inkremen B = " + B);
        
        //Dekremen
        --A;
        B--;
        System.out.println("Dekremen A = " + A);
        System.out.println("Dekremen B = " + B);
        
    }
}